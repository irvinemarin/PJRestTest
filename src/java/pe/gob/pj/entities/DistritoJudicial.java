public class DistritoJudicial {

    float N_ID_DISTRITO;
    String C_DISTRITO;
    String X_NOM_DISTRITO;
    String L_FLAG_ACTIVO;

    public DistritoJudicial() {
    }

    public float getN_ID_DISTRITO() {
        return N_ID_DISTRITO;
    }

    public void setN_ID_DISTRITO(float N_ID_DISTRITO) {
        this.N_ID_DISTRITO = N_ID_DISTRITO;
    }

    public String getC_DISTRITO() {
        return C_DISTRITO;
    }

    public void setC_DISTRITO(String C_DISTRITO) {
        this.C_DISTRITO = C_DISTRITO;
    }

    public String getX_NOM_DISTRITO() {
        return X_NOM_DISTRITO;
    }

    public void setX_NOM_DISTRITO(String X_NOM_DISTRITO) {
        this.X_NOM_DISTRITO = X_NOM_DISTRITO;
    }

    public String getL_FLAG_ACTIVO() {
        return L_FLAG_ACTIVO;
    }

    public void setL_FLAG_ACTIVO(String L_FLAG_ACTIVO) {
        this.L_FLAG_ACTIVO = L_FLAG_ACTIVO;
    }
}
